// Kategori dihitung berdasarkan persentase dari skor maksimum,
// supaya tetap akurat berapa pun jumlah soalnya (saat ini 10 soal x skor maks 10 = 100).
export function getResultCategory(score, totalQuestions = quizQuestions.length) {
  const maxScore = totalQuestions * 10
  const ratio = score / maxScore

  if (ratio >= 0.875) return resultCategories[0]
  if (ratio >= 0.625) return resultCategories[1]
  if (ratio >= 0.375) return resultCategories[2]
  return resultCategories[3]
}

